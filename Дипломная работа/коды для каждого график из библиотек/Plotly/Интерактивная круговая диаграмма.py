import plotly.express as px  # Импортируем Plotly Express для создания графиков

# Загрузка набора данных Iris
iris = px.data.iris()  # Загружаем встроенный набор данных Iris

# Создание интерактивной круговой диаграммы
fig = px.pie(
    iris,
    names='species',  # Указываем названия для секторов (виды ирисов)
    title='Интерактивная круговая диаграмма: Распределение видов ирисов'  # Заголовок диаграммы
)

fig.show()  # Отображаем график