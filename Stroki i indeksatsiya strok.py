# В переменную записывается любая строка
example = 'Хороший день для учебы'

# первый символ строки
print(example[0])

# последний символ строки
print(example[-1])

# Новая переменная с текстом и вывод второй половины
example = 'Urban'
print(example[2:])

# Возвращение значения переменной изначально, строка наоборот и каждый второй символ
example = 'Хороший день для учебы'
print(example[::-1])
print(example[::2])