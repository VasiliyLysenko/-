''' Я выбрал библиотеки для ознакомления requests, pandas и matplotlib. Начнем с библиотеки requests. '''

import requests
import pandas as pd
import matplotlib as plt

''' Основные возможности requests:
    1. Упрощение работы с HTTP-запросами
    2. Поддержка различных методов запросов (GET, POST, PUT, DELETE и др.)
    3. Простой и ясный интерфейс для работы с ответами от сервера.
'''

# Пример использования:

# Выполняем GET-запрос
r = requests.get('https://api.github.com/events')
# Выводим статус-код
print("Статус-код:", r.status_code)
# Выводим данные в формате JSON
data = r.json()
print("Данные:", data)

''' Основные возможности pandas:
    1. Обработка и анализ данных в виде таблиц (DataFrame)
    2. Чтение и запись данных в различные форматы (CSV, Excel, SQL и др.)
    3. Множество встроенных функций для манипуляции данными (фильтрация, группировка и т.д.)
'''

# Пример использования:

# Читаем данные из CSV-файла
data = pd.read_csv('data.csv')

# Выводим первые 5 строк
print("Первые 5 строк:\n", data.head())

# Выполняем простой анализ: выводим среднее значение по столбцу
average_value = data['значение'].mean()
print("Среднее значение:", average_value)

''' Основные возможности matplotlib:
    1. Визуализация данных в различных форматах (гистограммы, линейные графики, круговые диаграммы и др.)
    2. Настройка внешнего вида графиков (цвета, метки, заголовки и т.д.)
    3. Возможность сохранения графиков в разных форматах (PNG, PDF и др.)
'''

# Данные для визуализации
x = [1, 2, 3, 4, 5]
y = [10, 20, 25, 30, 35]

# Создание графика
plt.plot(x, y, marker='o')
plt.title('Пример графика')
plt.xlabel('X-axis')
plt.ylabel('Y-axis')
plt.grid()
plt.show()

''' В заключении:
    1. requests - позволяет легко взаимодействовать с web-сервисами, получать данные из API.
    2. pandas - предоставляет удобные методы для анализа и обработки данных.
    3. matplotlib - делает возможно визуализировать данные, что является важным аспектом в анализе и представлении информации.
'''